export function t(lang, key) {
  return translations[lang]?.[key] || translations.en[key] || key;
}

export function getLoadingSteps(lang) {
  const g = (k) => t(lang, k);
  return [
    { text: g("loading1"), color: "#666", delay: 0 },
    { text: g("loading2"), color: "#888", delay: 700 },
    { text: g("loading3"), color: "#888", delay: 1400 },
    { text: g("loading4"), color: "#ffd60a", delay: 2000 },
    { text: g("loading5"), color: "#ff6b35", delay: 2600 },
    { text: g("loading6"), color: "#ff1e3c", delay: 3000 },
  ];
}

